package com.harreke.easyapp.controllerlayout;

/**
 * 由Harreke于2016/1/5创建
 */
public enum AutoResponse {
    /**
     * 自动响应显示与隐藏
     */
    Show_Hide,
    /**
     * 只自动响应显示，不自动响应隐藏
     */
    Show,
    /**
     * 只自动响应隐藏，不自动响应显示
     */
    Hide,
    /**
     * 既不自动响应显示，也不自动响应隐藏
     */
    None
}